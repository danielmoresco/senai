#Como calcular a quantidade de novelos de lã necessários para produzir cada blusa em uma confecção, considerando
#que cada blusa requer uma quantidade de 120 metros de fio e que cada novelo contém 125 de metros de fio?

blusas = int(input("Olá, insira a quantidade de blusas que você deseja: "))

novelo = (blusas * 120) / 125

print ("Novelos de lã necessarios: ",novelo)







